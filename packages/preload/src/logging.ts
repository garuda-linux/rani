export function trace(msg: string): void {
  console.debug(msg);
}

export function debug(msg: string): void {
  console.debug(msg);
}

export function info(msg: string): void {
  console.info(msg);
}

export function warn(msg: string): void {
  console.warn(msg);
}

export function error(msg: string): void {
  console.error(msg);
}
